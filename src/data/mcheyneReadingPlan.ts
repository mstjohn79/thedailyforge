// M'Cheyne One-Year Bible Reading Plan
// Robert Murray M'Cheyne (1813-1843) designed this plan to read through
// the New Testament and Psalms twice, and the Old Testament once per year.
// 4 readings per day: 2 for family worship, 2 for private devotion

export interface McheyneDay {
  day: number;
  readings: {
    family1: string;  // Family reading 1 (usually OT)
    family2: string;  // Family reading 2 (usually NT)
    private1: string; // Private reading 1 (OT)
    private2: string; // Private reading 2 (NT/Psalms)
  };
}

