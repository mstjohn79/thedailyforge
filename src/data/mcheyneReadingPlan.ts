// Helper to get day of year (1-365)
export function getDayOfYear(date: Date = new Date()): number {
  const start = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  return Math.floor(diff / oneDay);
}

// Get readings for a specific day
export function getMcheyneDay(dayNumber: number): McheyneDay | null {
  if (dayNumber < 1 || dayNumber > 365) return null;
  return MCHEYNE_PLAN[dayNumber - 1];
}

// Get today's readings based on calendar
export function getTodaysMcheyneReadings(): McheyneDay {
  const dayOfYear = getDayOfYear();
  return MCHEYNE_PLAN[dayOfYear - 1] || MCHEYNE_PLAN[0];
}

// Book display names
export const BOOK_DISPLAY_NAMES: Record<string, string> = Object.fromEntries(
  Object.entries(BOOK_MAP).map(([name, id]) => [id, name])
);

// Format a reading reference for display (e.g., "GEN.1" -> "Genesis 1")
export function formatReadingDisplay(reading: string): string {
  const [bookId, chapter] = reading.split('.');
  const bookName = BOOK_DISPLAY_NAMES[bookId] || bookId;
  return `${bookName} ${chapter}`;
}
