// Helper to format reading as "BOOK.CHAPTER" for our system
function formatReading(book: string, chapter: number): string {
  const bookId = BOOK_MAP[book];
  if (!bookId) {
    console.warn(`Unknown book: ${book}`);
    return `${book}.${chapter}`;
  }
  return `${bookId}.${chapter}`;
}

